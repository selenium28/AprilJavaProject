package basic;

public class DataTypesConcept {  // Class Body

	// Starting point of execution 
	public static void main(String[] args) { // Method Body
		
		// 1. int DataTypes
		int i = 10; // int - DataType, i - Variable, 10 - value
		System.out.println(i);
		int m = 60; // ; - Semicolon 
//		int n = -10.98;  //  cannot convert from double to int
		int k = -20;
		System.out.println(k +" " + " " +m);
		System.out.println(k+m);
		
		
		// 2. double DataTypes
		double d = 100.56;
		double d1 = 33.33;
		System.out.println(d);
		double d2 = 200;
		System.out.println(d2);
		
		// 3. char DataTypes
		char c = 'A';
		char c1 = 'a';
		char c2 = '@';
		System.out.println(c1);
		char c3 = '1';
		
		System.out.println(d+c);
		
		// 4. Boolean DataTypes
		boolean b = true;
		boolean b1 = false;
		System.out.println(b);
		
		// 5. String Datatypes
		String str = "Medha";
		String str2 = "123Java";
		String str5 = "!@#$%^&";
		System.out.println(str2+m);

	}

}
