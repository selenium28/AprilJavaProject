package basic;

public class ForLoopConcept {

	public static void main(String[] args) {
		
		// int i = 1 - Initialization, i <= 10 - Condition, i++ - Increment
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		
		System.out.println("***********");
		
		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}

	}

}
