package basicCore;

public class DataTypesClass {  // class body

//	Starting point of execution
	public static void main(String[] args) {  // Method body
		
		// 1. int Datatype
		// int - datatype, a - variable, 10 - value
		int a = 10;
		int b = -20;
//		int b = 30; Duplicate variables are not allowed in Java.
		
		System.out.println(b);
		
		// 2. double DataType
//		double -> datatype, d - variable, 33.33 - value
		double d = 33.33;
		double d1 = 100;
		
		System.out.println(d);
		System.out.println(d1);
		
		// 3. char Datatype
		char c = 'A';
		char c1 = 'a';
		System.out.println(c1);
		
		// 4. boolean DataType
		boolean b1 = true;
		boolean b2 = false;
		
		// 5. String - DataType  as well as --> Class
		String str = "Prachi";
		String str2 = "123AKSL";
		String str3 = "!@#$%^&*()";
		
		System.out.println(str2);
		System.out.println(str3);	

	}

}
