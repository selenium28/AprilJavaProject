package basicCore;

public class ForLoopConcept {

	public static void main(String[] args) {
		// Initialization, Condition, Increment 
		// Print 2 to 20 --> 2,4,6,8.... by using whileloop & forloop.
		for (int i = 1; i <=10; i++) {
			System.out.println(i);
			
		}
		System.out.println("************");
		
		for (int j = 10; j>=1; j--) {
			System.out.println(j);
		}
		

	}

}
