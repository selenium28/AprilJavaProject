package oops;

public class ChildClass extends ParentClass{
	// It is inheriting the properties o parent class into child class is known as Inheritance.
	// When same method is present in child class as well as in parent class with same no of parameters is known as Method overriding.

	public void scooty() {
		System.out.println("ChildClass --> Scooty method");
	}
	
	public void bike() {
		System.out.println("ChildClass --> Bike method");
	}

}
